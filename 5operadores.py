primeiro_numero = float(input("Digite um número: "))
segundo_numero = float(input("Digite outro número: "))

#ADIÇÃO
soma = primeiro_numero + segundo_numero
print(f'A soma dos dois números é: {soma}!')

#SUBSTRAÇÃO
sub = primeiro_numero - segundo_numero
print(f'A sub dos dois números é: {sub}!')


#MULTIPLICAÇÃO
mult = primeiro_numero * segundo_numero
print(f'A mult dos dois números é: {mult}!')

#DIVISÃO
div = primeiro_numero / segundo_numero
print(f'A div dos dois números é: {div}!')

#RESTO
rest = primeiro_numero % segundo_numero
print(f'O rest dos dois números é: {rest}!')