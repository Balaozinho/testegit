primeiro_numero = float(input("Digite um número: "))
segundo_numero = float(input("Digite outro número: "))

#MAIOR QUE
print(primeiro_numero > segundo_numero)

#MENOR QUE
print(primeiro_numero < segundo_numero)

#MAIOR OU IGUAL
print(primeiro_numero >= segundo_numero)

#MENOR OU IGUAL
print(primeiro_numero <= segundo_numero)

#IGUAL
print(primeiro_numero == segundo_numero)

#DIFERENTE DE
print(primeiro_numero != segundo_numero)